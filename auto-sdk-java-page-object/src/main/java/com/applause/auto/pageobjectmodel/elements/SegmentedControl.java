package com.applause.auto.pageobjectmodel.elements;

import com.applause.auto.context.IPageObjectContext;
import com.applause.auto.pageobjectmodel.base.BaseElement;
import com.applause.auto.pageobjectmodel.base.LocatedBy;
import com.applause.auto.pageobjectmodel.factory.LazyWebElement;
import io.appium.java_client.ios.IOSDriver;

/**
 * BaseElement subclass representing a SegmentedControl - an iOS-specific UI element.
 *
 * @see com.applause.auto.pageobjectmodel.base.BaseElement
 */
public class SegmentedControl extends BaseElement {
  /**
   * Basic constructor for a SegmentedControl. SegmentedControls generally should be generated by
   * PageObjectBuilder.
   *
   * @param element the LazyWebElement underlying this SegmentedControl
   * @param context the web driver
   */
  public SegmentedControl(final LazyWebElement element, final IPageObjectContext context) {
    super(element, context);
  }

  /**
   * Gets the currently selected Button in the SegmentedControl.
   *
   * @return The currently selected button, as a Button element.
   */
  public Button getSelectedButton() {
    if (!(context.getDriver() instanceof IOSDriver)) {
      throw new UnsupportedOperationException(
          "SegmentedControl is an iOS-specific element. Not supported on " + "other platforms.");
    }
    return this.getChild(LocatedBy.iOSNsPredicate("value == '1'"), Button.class);
  }
}
